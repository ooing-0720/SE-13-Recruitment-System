/*
* 지원 취소 기능
* 현재 진행중인 채용 정보에 대해 지원 취소
*/
void Apply::cancelApply(Member member) {
	for (int i = 0; i < applyCount - 1; i++) {
		if (applier[i].getId() == member.getId()) {
			applier[i] = applier[i + 1];
		}
	}
	// 회원 한 명의 지원이 취소되었으므로 지원자 수 1 감소
	--applyCount;
}

