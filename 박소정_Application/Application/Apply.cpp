/*
* 일반 회원 지원 정보 통계 기능
* 본인이 지원한 정보에 대해 업무와 지원 횟수 출력
*/
void Apply::countWorkApplyByGeneral(Member member) {
	int count = 0;
	for (int i = 0; i < applyCount; i++) {
		if (applier[i].getId() == member.getId())
			++count;
	}
	
	// 회원이 지원한 경우 지원 정보 출력
	if (count != 0) {
		cout << work << " " << count << endl;
	}
}